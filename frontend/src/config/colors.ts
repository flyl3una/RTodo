/**
 * 默认颜色
 */
export const DEFAULT_COLOR = '#3B82F6'; // blue-500

/**
 * 获取随机颜色
 * @param excludeColors 要排除的颜色列表
 */
export function getRandomColor(excludeColors: string[] = []): string {
  const availableColors = ALL_COLORS.filter(
    color => !excludeColors.includes(color)
  );

  if (availableColors.length === 0) {
    return DEFAULT_COLOR;
  }

  const randomIndex = Math.floor(Math.random() * availableColors.length);
  return availableColors[randomIndex];
}
